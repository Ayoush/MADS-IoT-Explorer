defmodule AcqdatCore.Mailer.NotificationEmailTest do
  use ExUnit.Case
  use AcqdatCore.DataCase
  use Bamboo.Test

  import AcqdatCore.Support.Factory
  alias AcqdatCore.Mailer.NotificationEmail
  alias AcqdatCore.Mailer

  test "send email" do
    device = insert(:device)
    message_list = [%{"Temperature" => %{"humid" => 35}}, %{"Temperature" => %{"temp" => 90}}]

    email = NotificationEmail.email(device, message_list)
    email |> Mailer.deliver_now()
    assert_delivered_email(email)
  end
end
